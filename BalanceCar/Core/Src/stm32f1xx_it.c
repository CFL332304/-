/* USER CODE BEGIN 0 */
static void USART_IdleRxHandler(UART_HandleTypeDef *huart)
{
    uint16_t dmaCounter;
    uint16_t receivedLength;
    uint16_t copyLength;
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;

    if (huart == NULL)
    {
        return;
    }

    if (__HAL_UART_GET_FLAG(huart, UART_FLAG_IDLE) == RESET)
    {
        return;
    }

    __HAL_UART_CLEAR_IDLEFLAG(huart);
    HAL_UART_DMAStop(huart);

    dmaCounter = __HAL_DMA_GET_COUNTER(huart->hdmarx);
    receivedLength = sizeof(RDataBuff) - dmaCounter;

    if ((SerialRDataQueue != NULL) &&
        (receivedLength > 2U) &&
        (RDataBuff[0] == '[') &&
        (RDataBuff[receivedLength - 1U] == ']'))
    {
        copyLength = receivedLength - 2U;
        if (copyLength >= sizeof(g_SerialRdataPacket))
        {
            copyLength = sizeof(g_SerialRdataPacket) - 1U;
        }

        memset(g_SerialRdataPacket, 0, sizeof(g_SerialRdataPacket));
        memcpy(g_SerialRdataPacket, &RDataBuff[1], copyLength);

        if (xQueueSendFromISR(SerialRDataQueue, &g_SerialRdataPacket,
                              &xHigherPriorityTaskWoken) == pdTRUE)
        {
            portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
        }
    }

    memset(RDataBuff, 0, sizeof(RDataBuff));
    (void)HAL_UART_Receive_DMA(huart, (uint8_t *)RDataBuff, sizeof(RDataBuff));
}
